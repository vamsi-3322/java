import java.io.*;
import java.util.*;
public class AsciiValues
{
    public static void main(String args[])
    {
        char ch1='a';
        char ch2='b';
        int asciivalue1=ch1;
        int asciivalue2=ch2;
        System.out.println("The ASCII VALUE of "+ch1+" is " +asciivalue1);
        System.out.println("The ASCII VALUE of "+ch2+ " is " +asciivalue2);
    }
}
    
